package ca.seantyler.gameengine.entities;

public abstract class AbstractEntity implements IEntity {

	private int x, y;

	public AbstractEntity(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public void setX(int x) {
		this.x = x;
	}

	public void setY(int y) {
		this.y = y;
	}

}
